@implementation CEViewController {
    CEObject *_testObject;
    CERenderer *_renderer;
}

- (void)viewDidLoad {
    [super viewDidLoad];
    
    _testObject = [CEObject new];
    _testObject.position = GLKVector3Make(0, 0, 0);
    _testObject.transformMatrix = GLKMatrix4Identity;
    _testObject.vertexData = [NSData dataWithBytes:gCubeVertexData length:sizeof(gCubeVertexData)];
    
    _renderer = [CERenderer new];
    GLKView *view = (GLKView *)self.view;
    view.context = _renderer.context;
    view.drawableDepthFormat = GLKViewDrawableDepthFormat24;
}

static float rotation = 0;
- (void)update {
    rotation += self.timeSinceLastUpdate * 0.5;
}

- (void)glkView:(GLKView *)view drawInRect:(CGRect)rect {
    _testObject.transformMatrix = GLKMatrix4MakeRotation(rotation, 1, 1, 1);
    [_renderer renderObject:_testObject];
}


@end
